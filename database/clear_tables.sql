-- =====================================================
-- Безопасная очистка таблиц EkiTili
-- =====================================================

-- ВАЖНО: Выполняйте в правильном порядке!
-- Сначала удаляем данные из дочерних таблиц (с внешними ключами),
-- затем из родительских таблиц

-- Шаг 1: Откатить все незавершенные транзакции
ROLLBACK;

-- Шаг 2: Удалить все результаты тестов
DELETE FROM TEST_RESULTS;

-- Шаг 3: Подтвердить удаление результатов тестов
COMMIT;

-- Шаг 4: Теперь можно безопасно удалить пользователей
DELETE FROM USER_ACCOUNTS;

-- Шаг 5: Подтвердить удаление пользователей
COMMIT;

-- Шаг 6 (опционально): Сбросить последовательности на начальное значение
ALTER SEQUENCE USER_ID_SEQ RESTART START WITH 1;
ALTER SEQUENCE TEST_RESULT_ID_SEQ RESTART START WITH 1;

-- Проверка: должны быть пустые таблицы
SELECT 'USER_ACCOUNTS: ' || COUNT(*) AS user_count FROM USER_ACCOUNTS;
SELECT 'TEST_RESULTS: ' || COUNT(*) AS test_count FROM TEST_RESULTS;

-- Все готово! Таблицы очищены, можно начинать заново.

